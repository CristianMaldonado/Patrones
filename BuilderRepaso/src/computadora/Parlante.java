package computadora;

public class Parlante {

	private int cantidadAltavoces;
	
	public Parlante(int cant) {
		this.setCantidadAltavoces(cant);
	}

	public int getCantidadAltavoces() {
		return cantidadAltavoces;
	}

	public void setCantidadAltavoces(int cantidadAltavoces) {
		this.cantidadAltavoces = cantidadAltavoces;
	}
	
	
}
